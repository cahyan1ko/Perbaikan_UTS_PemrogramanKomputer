    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String nimInput = jTextField1.getText().trim();

    if (nimInput.isEmpty()) {
        JOptionPane.showMessageDialog(this, "NIM belum diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int nimToSearch = Integer.parseInt(nimInput);

        Connection conn = koneksi.getConnection();

        if (conn != null) {
            String query = "SELECT * FROM mhs WHERE nim = ?";
            PreparedStatement statement = conn.prepareStatement(query);

            statement.setInt(1, nimToSearch);

            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                String nama = resultSet.getString("nama");
                String email = resultSet.getString("email");

                jLabel4.setText(nama);
                jLabel5.setText(Integer.toString(nimToSearch));
                jLabel6.setText(email);
            } else {
                jLabel4.setText("");
                jLabel5.setText("");
                jLabel6.setText("");
                JOptionPane.showMessageDialog(this, "NIM tidak terdaftar.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }

            statement.close();
            resultSet.close();
        } else {
            System.out.println("Tidak dapat membuat koneksi.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "NIM harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } catch (SQLException e) {
        System.out.println("Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

